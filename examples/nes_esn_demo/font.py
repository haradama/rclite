def char_to_tile_bytes(rows) -> bytes:
    """8x8 glyph -> 16 bytes NES pattern (plane0 = pixels, plane1 = 0)."""
    plane0 = bytearray()
    for r in rows:
        b = 0
        for c in range(8):
            if c < len(r) and r[c] == "#":
                b |= 0x80 >> c
        plane0.append(b)
    return bytes(plane0) + bytes(8)


def build_font() -> bytes:
    """CHR data for ASCII 0x20..0x5F (64 glyphs, 16 bytes each)."""
    out = bytearray()
    for code in range(0x20, 0x60):
        ch = chr(code)
        out += char_to_tile_bytes(_G.get(ch, _G[" "]))
    return bytes(out)


